// ---- ENGINE ----
let currentLang = localStorage.getItem('modelai_lang') || 'en';

function t(key) {
  return (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key]) ||
         (TRANSLATIONS['en'] && TRANSLATIONS['en'][key]) || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.innerHTML = val;
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
}

function setLang(code) {
  currentLang = code;
  localStorage.setItem('modelai_lang', code);
  const lang = LANGUAGES.find(l => l.code === code);
  if (lang) {
    const flagEl = document.getElementById('langFlag');
    const codeEl = document.getElementById('langCode');
    if (flagEl) flagEl.textContent = lang.flag;
    if (codeEl) codeEl.textContent = lang.code.toUpperCase();
  }
  applyTranslations();
  document.getElementById('langDropdown').classList.remove('open');
}

function buildLangDropdown() {
  const dd = document.getElementById('langDropdown');
  if (!dd) return;
  dd.innerHTML = LANGUAGES.map(l => `
    <div class="lang-option ${l.code === currentLang ? 'active' : ''}" onclick="setLang('${l.code}')">
      <span>${l.flag}</span><span>${l.name}</span>
    </div>
  `).join('');
}

document.addEventListener('DOMContentLoaded', () => {
  buildLangDropdown();
  setLang(currentLang);

  const btn = document.getElementById('langBtn');
  const dd = document.getElementById('langDropdown');
  if (btn && dd) {
    btn.addEventListener('click', e => {
      e.stopPropagation();
      dd.classList.toggle('open');
    });
    document.addEventListener('click', () => dd.classList.remove('open'));
  }
});
